import * as React from "react"
import { clsx, type ClassValue } from "clsx"
import { twMerge } from "tailwind-merge"

function cn(...inputs: ClassValue[]) {
    return twMerge(clsx(inputs))
}

const Card = React.forwardRef<
    HTMLDivElement,
    React.HTMLAttributes<HTMLDivElement>
>(({ className, ...props }, ref) => (
    <div
        ref={ref}
        className={cn(
            "rounded-xl border border-border bg-surface/50 backdrop-blur-sm text-foreground shadow-sm transition-all duration-300 hover:border-border/80 hover:bg-surface/70 hover:shadow-md hover:shadow-primary/5",
            className
        )}
        {...props}
    />
))
Card.displayName = "Card"

